String convertDigit(String m) {
  switch(m){
    case "1":
      return "۱";
    case "2":
      return "۲";
    case "3":
      return "۳";
    case "4":
      return "۴";
    case "5":
      return "۵";
    case "6":
      return "۶";
    case "7":
      return "۷";
    case "8":
      return "۸";
    case "9":
      return "۹";
    case "0":
      return "۰";

  }
  return m;
}
